/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	setbuf(stdout, NULL);

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_USART1_UART_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
	RADIO1_Init();
	SERVO1_Init();

	// Set the channel, Power amplification, and data rate
	NRF24_SetDataRate(&radio1, NRF24_DATARATE_250KBPS);
	NRF24_SetPA(&radio1, NRF24_PA_MIN);

	// If we are configured for Transmit mode
	if(ROLE == TX_MODE){
		// Set up in TX Mode
		NRF24_TXMode(&radio1, (uint8_t*)ADDRESS, CHANNEL);
	}
	// Or if we are configured for Receive mode
	else if (ROLE == RX_MODE){
		// Configure in RX mode with the channel we'd like to receive on
		NRF24_RXMode(&radio1, (uint8_t*)ADDRESS, CHANNEL);
		NRF24_SetPayloadWidth(&radio1, NRF24_PIPE1, sizeof(joystick1));
	}

	// Calibrate the ADC
	HAL_ADCEx_Calibration_Start(&hadc1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

		// If we are in Transmit mode
		if(ROLE == TX_MODE){
			// Get the current state of the joystick
			ReadJoystickState();

			// Print info about the struct
			printf("Vx: %d, Vy: %d, SW: %d\n\r", joystick1.iVX, joystick1.iVY, joystick1.iSW);

			// Transmit the current state of the joystick
			NRF24_Transmit(&radio1, (uint8_t*)(&joystick1), sizeof(joystick1));

			// Wait a second
			HAL_Delay(10);

			if(DEBUG_PRINT){
				printf("Info after transmit:\n\r");
				NRF24_PrintDebugInfo(&radio1);

				// Wait for a second before the next transmission
				HAL_Delay(1000);
			}
		}
		// Or if we are in Receive mode
		else if (ROLE == RX_MODE){
			// Receive the data and store it in the buffer
			NRF24_Receive(&radio1, (uint8_t*)(&joystick1));

			// Display the values
			printf("Vx: %d, Vy: %d, SW: %d\n\r", joystick1.iVX, joystick1.iVY, joystick1.iSW);

			// Get the angle that we want to move the joystick to
			float fAngle = map(joystick1.iVX, 0, 0xFFF, 0, 180);

			// Move the servo to the specified spot
			SERVO_MoveTo(&servo1, fAngle);

			// Wait a second
			HAL_Delay(5);

			if(DEBUG_PRINT){
				printf("Info after receive:\n\r");
				NRF24_PrintDebugInfo(&radio1);

				// Delay for a bit
				HAL_Delay(1000);
			}
		}

	}
  /* USER CODE END 3 */
}

