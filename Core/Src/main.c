// Get the current state of the joystick and update the struct accordingly
void ReadJoystickState(){
	// GET ADC INPUT

	// Read from the ADC
	HAL_ADC_Start_DMA(&hadc1, (uint32_t*)joystickResultsADC, 2);

	// Loop until a conversion is complete
	while(!adcConversionComplete){}

	// Reset the flag
	adcConversionComplete = 0;

	// GET SWITCH BUTTON INPUT

	// Read the state of the switch button
	uint8_t iSWState = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_15);

	// Print to the console
	//printf("Val 1: %d, Val 2: %d, SW: %d\n\r", joystickResultsADC[0], joystickResultsADC[1], iSWState);

	// Set the values in the struct accordingly
	joystick1.iVX = joystickResultsADC[0];
	joystick1.iVY = joystickResultsADC[1];
	joystick1.iSW = iSWState;
}

